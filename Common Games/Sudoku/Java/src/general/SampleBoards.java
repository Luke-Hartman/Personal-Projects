	public static String toString(int[][] matrix) {
		final int L = matrix.length;
		final int S = (int) Math.sqrt(L);
		String s = "{";
		for(int i = 0; i < L; i++) {
			s += "{";
			for(int j = 0; j < L; j++) {
				s += matrix[i][j];
				if(j < L - 1) {
					s += ",";
					if((j + 1) % S == 0) {
						s += " ";
					}
				}
			}
			s += "}";
			if(i < L - 1) {
				s += ",\n ";
				if((i + 1) % S == 0) {
					s += "\n ";
				}
			}
		}
		s += "};\n\n";
		return s;
	}
